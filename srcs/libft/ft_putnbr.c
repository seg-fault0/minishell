#include "minishell.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n == -2147483648)
	{
		write (STDOUT, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write (STDOUT, "-", 1);
	}
	if (n >= 0 && n <= 9)
	{
		c = n + '0';
		write (STDOUT, &c, 1);
	}
	else
	{
		ft_putnbr (n / 10);
		ft_putnbr (n % 10);
	}
}
